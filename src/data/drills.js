// =====================================================================
// Practice question bank — feeds two games:
//   • Choice  (選擇題) : renders `distractors` + `solution` as options
//   • Input   (手寫題) : checks typed command against `accept` regexes
//
// `modes` says which games a question can appear in. Most drills work in
// both; pure-concept questions are choice-only.
//
// Input judging is "normalized string comparison": before matching we
// trim, collapse runs of whitespace to one space, and normalize smart
// quotes to ". So spacing, hyphens and argument order still matter — but
// double vs single quotes and extra spaces don't trip the learner up.
// `accept` patterns are written against that normalized form.
// =====================================================================

